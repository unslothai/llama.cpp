"""Compare top-k logprobs from vLLM and llama-server at multiple decode positions.

Drives the conversation forward in lockstep: at each step, take the assistant
text generated by vLLM up to position N, feed it as a fixed prefix to both
servers via the chat history, and ask each for top_logprobs at the next token.
Quantifies how much the two distributions diverge as the decode position
grows.
"""
import json
import os
from datetime import datetime, timedelta
from pathlib import Path

from huggingface_hub import hf_hub_download
from openai import OpenAI


