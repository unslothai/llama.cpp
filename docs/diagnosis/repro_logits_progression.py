    today = datetime.today().strftime("%Y-%m-%d")
    yesterday = (datetime.today() - timedelta(days=1)).strftime("%Y-%m-%d")
    return raw.format(name="Mistral-Medium-3.5-128B", today=today, yesterday=yesterday)


